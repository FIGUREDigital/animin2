using UnityEngine;
using System.Collections;

public class Alarm : MonoBehaviour {

	private LoopingScrollView m_HoursSelector;
	private LoopingScrollView m_MinutesSelector;

	// Use this for initialization
	void Start () {
		LoopingScrollView[] loopers = GetComponentsInChildren<LoopingScrollView> ();
		for (int i = 0; i < loopers.Length; i++) {
			if (loopers[i].gameObject.name == "HoursScrollView") m_HoursSelector = loopers[i];
			else if (loopers[i].gameObject.name == "MinutesScrollView") m_MinutesSelector = loopers[i];
		}
	}
	
	// Update is called once per frame
	void Update () {
		//Debug.Log ("Alarm is currently set to time: [" + m_HoursSelector.CurrentlySelected + ":" + m_MinutesSelector.CurrentlySelected + "];");
	}
}
